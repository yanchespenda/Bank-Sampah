    private void btnAksiEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAksiEditActionPerformed
        // TODO add your handling code here:
        
        System.out.println("Aksi: MDPerusahaanView | Edit");
        if (mainTable.getSelectedRowCount() > 0) {
            if (mainTable.getSelectedRowCount() == 1) {
                String getNamaCustumer = mainTable.getValueAt(mainTable.getSelectedRow(), 1).toString();
                sampahController.openEditData(getNamaCustumer).addWindowListener(new WindowAdapter() {
                    @Override
                    public void windowClosed(WindowEvent e) {
                        System.out.println("Aksi: MDPerusahaanView | openEditData | windowClosed | getRefreshList");
                        getRefreshList();
                    }
                });
            } else {
                JOptionPane.showMessageDialog(null, "Data yang di edit harus 1", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Tidak ada data yang di pilih", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAksiEditActionPerformed

    private void btnAksiHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAksiHapusActionPerformed
        // TODO add your handling code here:
        
        System.out.println("Aksi: MDPerusahaanView | Hapus");
        if (mainTable.getSelectedRowCount() > 0) {
            if (dialogMessage.tampilkanPilihan("Apa Anda yakin ingin menghapus data ini?","Aksi", new Object[]{"Ya","Tidak"}) == 0) {
                ArrayList<String> tasks = new ArrayList<>();
                int[] selectedRows = mainTable.getSelectedRows();
                for (int i = 0; i < selectedRows.length; i++) {
                    String taskAsArray = (String) (mainTable.getValueAt(mainTable.convertRowIndexToModel(selectedRows[i]), 1));
                    tasks.add(taskAsArray);
                }
                
                Gson gson = new GsonBuilder().serializeSpecialFloatingPointValues().serializeNulls().create();
                System.out.println(gson.toJson(tasks));
                
                sampahController.delete(tasks);
                getRefreshList();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Tidak ada data yang di pilih", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAksiHapusActionPerformed

