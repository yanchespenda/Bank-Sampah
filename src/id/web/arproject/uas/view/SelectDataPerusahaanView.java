    public void tampilkanData(Object[][] list) {
        mainTableModel.setRowCount(0);
        if ((list != null) && (list.length > 0)) {
            for (Object[] list1 : list) {
                mainTableModel.addRow(list1);
            }
        }
    }
    
    public String getSelectedData() {
        return dataSelected;
    }

