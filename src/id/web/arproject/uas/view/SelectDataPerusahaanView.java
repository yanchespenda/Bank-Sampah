    private void btnAksiPilihActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAksiPilihActionPerformed
        // TODO add your handling code here:
        
        if (mainTable.getSelectedRowCount() > 0) {
            if (mainTable.getSelectedRowCount() == 1) {
                dataSelected = mainTable.getValueAt(mainTable.getSelectedRow(), 1).toString();
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Data yang di pilih maksimal 1");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Belum ada data yang di pilih");
        }
    }//GEN-LAST:event_btnAksiPilihActionPerformed

